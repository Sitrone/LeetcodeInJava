package com.web.javax.domain;

public class User
{
	private String id;
	private String name;
	private String email;
	
	public String getId()
	{
		return id;
	}
	public void setId(String id2)
	{
		this.id = id2;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public String getEmail()
	{
		return email;
	}
	public void setEmail(String email)
	{
		this.email = email;
	}
	
	
}
